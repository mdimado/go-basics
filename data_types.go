package main

import "fmt"

func data_types() {
	//integer variables

	var x int8 = 74
	fmt.Println(x)

	var y int = 987654323456758976
	fmt.Println(y)

	//unsigned integers

	var a uint = 45
	fmt.Println(a)

	//float

	var f float32 = 404040.404
	fmt.Println(f)

	//string

	var name string = "Imad"
	fmt.Println(name)

	//boolean

	var isHappy bool = true
	fmt.Println(isHappy)

	//

}
